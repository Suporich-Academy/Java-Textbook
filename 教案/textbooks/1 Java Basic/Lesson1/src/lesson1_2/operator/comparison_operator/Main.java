package lesson1_2.operator.comparison_operator;

public class Main {

	public static void main(String[] args) {
		// 変数の初期化
		int a = 10;
		int b = 20;

		// aがbより小さい
		System.out.println(a < b);// ⇒true
		// aがbより大きい
		System.out.println(a > b);// ⇒false
		// aとbの値が等しい
		System.out.println(a == b);// ⇒false
		// aがb以上
		System.out.println(a >= b);// ⇒false
		// aがb以下
		System.out.println(a <= b);// ⇒true
		// aがbと等しくない
		System.out.println(a != b);// ⇒true

	}

}
