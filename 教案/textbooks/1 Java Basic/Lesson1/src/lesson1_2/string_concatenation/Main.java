package lesson1_2.string_concatenation;

public class Main {

	public static void main(String[] args) {
		// 名前を格納する変数を宣言
		String name = "Akemi";
		// 年齢を格納する変数を宣言
		int age = 30;
		// 趣味を格納する変数を宣言
		String hobby = "読書";

		// 連結して出力する
		System.out.println("私の名前は" + name + "です。　趣味は" + hobby + "です。年齢は" + age + "歳です。");
		// ⇒私の名前はAkemiです。 趣味は読書です。年齢は30歳です。
	}

}
